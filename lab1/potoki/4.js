let i = 0;
let j = 0;

// все нечетные числа через while
console.log("-----while-----");
while (i <= 49) {
  i++;
  console.log(i);
  i++;
}
console.log("-----while-----\n\n");

// все нечетные числа через for
console.log("-----for-----");
for (let j = 0; j <= 49; j++) {
  j++;
  console.log(j);
}
console.log("-----for-----");
